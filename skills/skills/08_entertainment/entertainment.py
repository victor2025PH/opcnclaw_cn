def tell_joke(**kwargs) -> dict:
    title, content = random.choice(JOKES)
    return {"title": title, "content": content, "type": "joke"}


def tell_riddle(**kwargs) -> dict:
    question, answer, category = random.choice(RIDDLES)
    return {
        "question": question,
        "answer": answer,
        "category": category,
        "hint": "想好了再看答案哦！",
    }


def tongue_twister(**kwargs) -> dict:
    text = random.choice(TONGUE_TWISTERS)
    return {"twister": text, "tip": "试着快速说三遍！"}
